# Inteiro para Float # 
preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)


# Float para Inteiro # 
preco = 10.30
print(preco)

preco = int(preco)
print(preco)


# Conversão por Divisão # 
preco = 10
print(preco)

print(preco / 2)
print (preco // 2)


# Númerico para String #
preco = 10.50
idade = 24 

print(str(preco))
print(str(idade))

texto = f"Idade: {idade} Preco: {preco}"
print(texto)


# String para Número #
preco = "10.50"
idade = "24"

print(float(preco))
print(int(idade))