# Fatiamento de strings é uma técnica utilizada para retornar substrings (partes da string original), informando início (start) fim (stop) e passo (step): [start: stop[,step]]#

nome = "Guilherme Klemann"

print(nome[0])
print(nome[:9])
print(nome[10:])
print(nome[10:16])
print(nome[10:16:2])
print(nome[:])
print(nome[::-1])