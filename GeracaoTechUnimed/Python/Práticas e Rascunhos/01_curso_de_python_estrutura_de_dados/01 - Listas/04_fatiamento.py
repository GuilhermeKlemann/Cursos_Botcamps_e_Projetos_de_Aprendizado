lista = ["P", "y", "t", "h", "o", "n"]

print(lista[2:]) # Exibe os elementos a partir do indíce 2
print(lista[:2]) # Exibe os elementos que antecedem o indíce 2
print(lista[1:3]) # Exibe os elementos do índice 1 e 3
print(lista[0:3:2]) # Exibe os elementos do índice 0 ao 3 em um intervalo de 2 em 2 ([start, stop, step])
print(lista[::]) # Exibe todos os elementos
print(lista[::-1]) # Exibe todos os elementos de trás para frente